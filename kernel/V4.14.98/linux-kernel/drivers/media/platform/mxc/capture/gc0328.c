/*!
 * gc0328 I2C probe function
 *
 * @param adapter            struct i2c_adapter *
 * @return  Error code indicating success or failure
 */
static int gc0328_probe(struct i2c_client *client,
			const struct i2c_device_id *id)
{
	struct pinctrl *pinctrl;
	struct device *dev = &client->dev;
	int retval;
	int i;
	int ret;
	u8 chip_id_high, chip_id_low;
	u16 dvp_retval = 0x00;
	u16 gc0328_id = 0x00;
	u8 gc0328_reso = 0x00;
	u8 gc0328_mode = 0x00;
	
	pr_info("gc0328_probe\n");

	/* gc0328 pinctrl */
	pinctrl = devm_pinctrl_get_select_default(dev);
	if (IS_ERR(pinctrl)) {
		dev_err(dev, "setup pinctrl failed\n");
		return PTR_ERR(pinctrl);
	}

	/* request power down pin */
	pwn_gpio = of_get_named_gpio(dev->of_node, "pwn-gpios", 0);
	if (!gpio_is_valid(pwn_gpio)) {
		dev_err(dev, "no sensor pwdn pin available\n");
		return -ENODEV;
	}
	retval = devm_gpio_request_one(dev, pwn_gpio, GPIOF_OUT_INIT_HIGH,
					"gc0328_pwdn");
	if (retval < 0)
		return retval;

	/* request reset pin */
	rst_gpio = of_get_named_gpio(dev->of_node, "rst-gpios", 0);
	if (!gpio_is_valid(rst_gpio)) {
		dev_err(dev, "no sensor reset pin available\n");
		return -EINVAL;
	}
	retval = devm_gpio_request_one(dev, rst_gpio, GPIOF_OUT_INIT_HIGH,
					"gc0328_reset");
	if (retval < 0)
		return retval;

	/* Set initial values for the sensor struct. */
	memset(&gc0328_data, 0, sizeof(gc0328_data));
	gc0328_data.sensor_clk = devm_clk_get(dev, "csi_mclk");
	if (IS_ERR(gc0328_data.sensor_clk)) {
		dev_err(dev, "get mclk failed\n");
		return PTR_ERR(gc0328_data.sensor_clk);
	}

	retval = of_property_read_u32(dev->of_node, "mclk",
					&gc0328_data.mclk);
	if (retval) {
		dev_err(dev, "mclk frequency is invalid\n");
		return retval;
	}

	retval = of_property_read_u32(dev->of_node, "mclk_source",
					(u32 *) &(gc0328_data.mclk_source));
	if (retval) {
		dev_err(dev, "mclk_source invalid\n");
		return retval;
	}

	retval = of_property_read_u32(dev->of_node, "csi_id",
					&(gc0328_data.csi));
	if (retval) {
		dev_err(dev, "csi_id invalid\n");
		return retval;
	}

	/* Set mclk rate before clk on */
	gc0328_set_clk_rate();

	clk_prepare_enable(gc0328_data.sensor_clk);
  
	gc0328_data.io_init = gc0328_reset;
	gc0328_data.i2c_client = client;
	gc0328_data.pix.pixelformat = V4L2_PIX_FMT_RGB565;
	gc0328_data.pix.width = 320;//240;  
	gc0328_data.pix.height = 240;//320;
	gc0328_data.streamcap.capability = V4L2_MODE_HIGHQUALITY |
					   V4L2_CAP_TIMEPERFRAME;
	gc0328_data.streamcap.capturemode = 0;
	gc0328_data.streamcap.timeperframe.denominator = DEFAULT_FPS;
	gc0328_data.streamcap.timeperframe.numerator = 1;

	//gc0328_regulator_enable(&client->dev);

	gc0328_reset();
	//pr_info("gc0328 flags 0x%x\n", client->flags);
	//pr_info("gc0328 addr 0x%x\n", client->addr);
	//pr_info("gc0328 name %s\n", client->name);
#if 0
	//get the device product ID
	u8 val;
	ret = gc0328_read_reg(0x0A, &val);
	pr_info("gc0328_read_reg prod id msb is %d\n", val);
	ret = gc0328_read_reg(0x0B, &val);
	pr_info("gc0328_read_reg prod id lsb is %d\n", val);
#endif
	//pr_info("gc0328 i2c address 1: 0x%x\n", gc0328_data.i2c_client->addr);
	retval = init_device();
	if (retval < 0) {
		clk_disable_unprepare(gc0328_data.sensor_clk);
		pr_warning("camera gc0328 init failed\n");
		gc0328_power_down(1);
		return retval;
	}
 
	/* create debug kobject */
        gc0328_kobj = kobject_create_and_add("gc0328", NULL);
        if (!gc0328_kobj) {
                ret = -ENOMEM;
                goto err_irq;
        }

        for (i = 0; i < ARRAY_SIZE(gc0328_attrs); i++) {
                ret = sysfs_create_file(gc0328_kobj, &gc0328_attrs[i].attr);
                if (ret != 0) {
                       // dev_err(gc0328->dev, "create index %d error\n", i);
                        goto err_irq;
                }
        }
	clk_disable(gc0328_data.sensor_clk);

	v4l2_i2c_subdev_init(&gc0328_data.subdev, client, &gc0328_subdev_ops);

	retval = v4l2_async_register_subdev(&gc0328_data.subdev);
	if (retval < 0)
		dev_err(&client->dev,
					"%s--Async register failed, ret=%d\n", __func__, retval);

	pr_info("camera gc0328, is found\n");
	err_irq:
	return retval;
}

/*!
 * gc0328 I2C detach function
 *
 * @param client            struct i2c_client *
 * @return  Error code indicating success or failure
 */
static int gc0328_remove(struct i2c_client *client)
{
	struct v4l2_subdev *sd = i2c_get_clientdata(client);

	v4l2_async_unregister_subdev(sd);

	clk_unprepare(gc0328_data.sensor_clk);

	gc0328_power_down(1);

	if (analog_regulator)
		regulator_disable(analog_regulator);

	if (core_regulator)
		regulator_disable(core_regulator);

	if (io_regulator)
		regulator_disable(io_regulator);

	return 0;
}

