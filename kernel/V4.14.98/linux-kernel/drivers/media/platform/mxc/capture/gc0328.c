/*!
 * gc0328_enum_framesizes - V4L2 sensor interface handler for
 *			   VIDIOC_ENUM_FRAMESIZES ioctl
 * @s: pointer to standard V4L2 device structure
 * @fsize: standard V4L2 VIDIOC_ENUM_FRAMESIZES ioctl structure
 *
 * Return 0 if successful, otherwise -EINVAL.
 */
static int gc0328_enum_framesizes(struct v4l2_subdev *sd,
			       struct v4l2_subdev_pad_config *cfg,
			       struct v4l2_subdev_frame_size_enum *fse)
{
	if (fse->index > gc0328_mode_MAX)
		return -EINVAL;

	fse->max_width =
			max(gc0328_mode_info_data[0][fse->index].width,
			    gc0328_mode_info_data[1][fse->index].width);
	fse->min_width = fse->max_width;
	fse->max_height =
			max(gc0328_mode_info_data[0][fse->index].height,
			    gc0328_mode_info_data[1][fse->index].height);
	fse->min_height = fse->max_height;
	return 0;
}

/*!
 * gc0328_enum_frameintervals - V4L2 sensor interface handler for
 *			       VIDIOC_ENUM_FRAMEINTERVALS ioctl
 * @s: pointer to standard V4L2 device structure
 * @fival: standard V4L2 VIDIOC_ENUM_FRAMEINTERVALS ioctl structure
 *
 * Return 0 if successful, otherwise -EINVAL.
 */
static int gc0328_enum_frameintervals(struct v4l2_subdev *sd,
		struct v4l2_subdev_pad_config *cfg,
		struct v4l2_subdev_frame_interval_enum *fie)
{
	int i, j, count;

	if (fie->index < 0 || fie->index > gc0328_mode_MAX)
		return -EINVAL;

	if (fie->width == 0 || fie->height == 0 ||
	    fie->code == 0) {
		pr_warning("Please assign pixel format, width and height.\n");
		return -EINVAL;
	}

	fie->interval.numerator = 1;

	count = 0;
	for (i = 0; i < ARRAY_SIZE(gc0328_mode_info_data); i++) {
		for (j = 0; j < (gc0328_mode_MAX + 1); j++) {
			if (fie->width == gc0328_mode_info_data[i][j].width
			 && fie->height == gc0328_mode_info_data[i][j].height
			 && gc0328_mode_info_data[i][j].init_data_ptr != NULL) {
				count++;
			}
			if (fie->index == (count - 1)) {
				fie->interval.denominator =
						gc0328_framerates[i];
				return 0;
			}
		}
	}

	return -EINVAL;
}

static int gc0328_set_clk_rate(void)
{
	u32 tgt_xclk;	/* target xclk */
	int ret;

	/* mclk */
	tgt_xclk = gc0328_data.mclk;
	tgt_xclk = min(tgt_xclk, (u32)gc0328_XCLK_MAX);
	tgt_xclk = max(tgt_xclk, (u32)gc0328_XCLK_MIN);
	gc0328_data.mclk = tgt_xclk;

	pr_warning("   Setting mclk to %d MHz\n", tgt_xclk / 1000000);
	ret = clk_set_rate(gc0328_data.sensor_clk, gc0328_data.mclk);
	if (ret < 0)
		pr_warning("set rate filed, rate=%d ret %d\n", gc0328_data.mclk, ret);
	return ret;
}

