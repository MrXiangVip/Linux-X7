/*!
 * dev_init - V4L2 sensor init
 * @s: pointer to standard V4L2 device structure
 *
 */
static int init_device(void)
{
	u32 tgt_xclk;	/* target xclk */
	u32 tgt_fps;	/* target frames per secound */
	enum gc0328_frame_rate frame_rate;
	int ret;

	gc0328_data.on = true;

	/* mclk */
	tgt_xclk = gc0328_data.mclk;

	/* Default camera frame rate is set in probe */
	tgt_fps = gc0328_data.streamcap.timeperframe.denominator /
		  gc0328_data.streamcap.timeperframe.numerator;

	if (tgt_fps == 15)
		frame_rate = gc0328_15_fps;
	else if (tgt_fps == 30)
		frame_rate = gc0328_30_fps;
	else
		return -EINVAL; /* Only support 15fps or 30fps now. */

	ret = gc0328_init_mode();

	return ret;
}

